#!/usr/bin/env python3
"""
Karen AI - Model Export Script

Exports a fine-tuned Karen AI model through the following pipeline:
  1. Merge LoRA adapter weights with the base model
  2. Save the merged model in safetensors format
  3. Convert to GGUF format via llama.cpp (with quantisation)
  4. Generate an Ollama Modelfile
  5. Optionally import into Ollama via ``ollama create``

Usage:
    python export_model.py --adapter-path ./output/karen-adapter --base-model meta-llama/Llama-3.2-1B
    python export_model.py --adapter-path ./output/karen-adapter --output-dir ./export --quantization Q5_K_M
    python export_model.py --adapter-path ./output/karen-adapter --ollama-import
"""

